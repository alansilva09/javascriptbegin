let spaceship = "Golias";
let velocity = 80;

console.log(velocity == 80) // ele vai mostrar que é TRUE

console.log(velocity === "80") // ele vai retornar FALSE por causa dos 3 sinais. "80" é string

console.log(velocity != 80) // vai retornar FALSE, 80 é igual a 80

console.log(velocity !== "80") // vai retornar TRUE pq vai considerar string com ! = =

console.log(velocity > 70) // TRUE, 80 é maior que 70

console.log(spaceship >  "Hemlet") // FALSE, pois é Golias vem antes de Hemlet (albfabeto)

console.log(velocity >= 90) //FALSE pois é menor que 90

console.log(spaceship >= "Golias") // vai retornar verdadeiro